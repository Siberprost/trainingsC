//3- Методы, которые ничего не принимают, но что-то возвращают

int method3() //int используется т.к. метод совершает возврат, в завсимости от того, что возвращается можем использовать и string
{
    return DateTime.Now.Year; //команда, указывающая дату сейчас, год- return значит, что параметр возвращаем
}
int year = method3(); // для того, чтобы отобразить возвращённое значение нужно дописать соотвтетсвующий указ. Вводим переменную год, которая будет реализоавть метод. 
Console.WriteLine(year);

//4- метод и принимает и возвращает
string method4(int count, string shlep) //string используется т.к. метод совершает возврат, в завсимости от того, что возвращается можем использовать и int
{
    int i = 0;
    string result = String.Empty;

    while(i < count)
    {
        result = result + shlep;
        i++; 
    }
    return result;
}

string res = method4(10, "шлёп");
Console.WriteLine(res);

//1- МЕТОДЫ которые НИЧЕГО не возвращают и НИЧЕГО не принимают
void method1()
{
    Console.WriteLine("made by SiberProst");
}
// пояснение- тут ничего не принято и не возвращено, метод просто указывает авторство
//как вызываются такие методы?
method1(); // важно указать скобки после названия метода и указать ;

//2- МЕТОДЫ которые НИЧЕГО НЕ ВОЗВРАЩАЮТ, но ПРИНИМАЮТ аргументы
void method2(string msg) // пришется воид и в скобках то, что может быть принято
{
    Console.WriteLine(msg); //как пример, просто сделали переменную msg, которая является строкой для текста (string)
}
method2("текст сообщения");

// приниматься может не одна переменная, их может быть несколько, если записать их через запятую с указанием типа (стринг, инт и тмоу подобные)

void method21(string msg, int count)
{
    int i = 0;
    while (i < count)
    {
        Console.WriteLine(msg);
        i++;
    }
}
method21("тесттестович" , 5); // в этом примере мы пишем слово тесттестович столько раз, сколько указано в count (вторая переменная)
